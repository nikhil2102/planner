public class Priority{
	private String name;
	private String self;
	private String iconUrl;
	private String id;

	public String getName(){
		return name;
	}

	public String getSelf(){
		return self;
	}

	public String getIconUrl(){
		return iconUrl;
	}

	public String getId(){
		return id;
	}
}
