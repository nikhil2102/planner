package com.kronos.sprint.planner.entity.capacity;

public class StatusCategory{
	private String colorName;
	private String name;
	private String self;
	private int id;
	private String key;

	public String getColorName(){
		return colorName;
	}

	public String getName(){
		return name;
	}

	public String getSelf(){
		return self;
	}

	public int getId(){
		return id;
	}

	public String getKey(){
		return key;
	}
}
